#include "libft.h"

int		ft_nb_digit_base_u(uintmax_t nb, int base)
{
	int i;

	i = 0;
	if (nb == 0)
		return (1);
	while (nb)
	{
		nb /= base;
		i++;
	}
	return (i);
}
