#include "libft.h"

int	ft_wchar_len(wchar_t wc)
{
	if (wc <= 0x7F)
		return (1);
	if (wc <= 0x7FF)
		return (2);
	if (wc <= 0x7FFF)
		return (3);
	return (4);
}
